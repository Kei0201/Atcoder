#include <bits/stdc++.h>
using namespace std;

int main () {
    int A, B;
    cin >> A >> B;

    
    // プログラムを追記
    cout << "A:";
    while (A > 0) {
        cout << "]";
        A--;
    }
    cout << endl;

    cout << "B:";
    while (B > 0) {
        cout << "]";
        B--;
    }
    cout << endl;
}