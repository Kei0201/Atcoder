#include <bits/stdc++.h>
using namespace std;

int main() {
    int name;

    // nameに10を代入
    name = 10;

    cout << name << endl;

    // nameに5を代入
    name = 5;

    cout << name << endl;

    // 変数aで入力を受け取る
    int a;
    cin >> a;

    cout << a * 10 << endl;

    // つなげて入力
    int b, c;
    cin >> b >> c;
    cout << b * c << endl;
}